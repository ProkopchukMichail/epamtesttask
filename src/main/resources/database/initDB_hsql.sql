DROP TABLE employe IF EXISTS;
DROP TABLE department IF EXISTS;
DROP SEQUENCE EMPLOYE_SEQ IF EXISTS ;
DROP SEQUENCE DEPARTMENT_SEQ IF EXISTS ;
COMMIT;
CREATE SEQUENCE EMPLOYE_SEQ AS INTEGER START WITH 5;
CREATE SEQUENCE DEPARTMENT_SEQ AS INTEGER START WITH 1;


CREATE TABLE department
(
  id INTEGER GENERATED BY DEFAULT AS SEQUENCE DEPARTMENT_SEQ PRIMARY KEY,
  departmentName VARCHAR(45),
  UNIQUE (departmentName)
);

CREATE TABLE employe
(
  id INTEGER GENERATED BY DEFAULT AS SEQUENCE EMPLOYE_SEQ PRIMARY KEY,
  departmentName VARCHAR(45),
  fullname VARCHAR(45),
  birthday DATE,
  salary INTEGER,
  CONSTRAINT FK_1 FOREIGN KEY (departmentName) REFERENCES department(departmentName) ON DELETE CASCADE ON UPDATE CASCADE
);
