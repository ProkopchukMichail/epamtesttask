DROP TABLE employes IF EXISTS;
DROP TABLE departments IF EXISTS;
DROP SEQUENCE EMPLOYEES_SEQ IF EXISTS ;
DROP SEQUENCE DEPARTMENTS_SEQ IF EXISTS ;
CREATE SEQUENCE EMPLOYEES_SEQ AS INTEGER START WITH 5;
CREATE SEQUENCE DEPARTMENTS_SEQ AS INTEGER START WITH 1;


CREATE TABLE departments
(
  id INTEGER GENERATED BY DEFAULT AS SEQUENCE DEPARTMENTS_SEQ PRIMARY KEY,
  departmentName VARCHAR(45)
/*  UNIQUE (departmentName)*/
);

CREATE TABLE employees
(
  id INTEGER GENERATED BY DEFAULT AS SEQUENCE EMPLOYEES_SEQ PRIMARY KEY,
 /* departmentName VARCHAR(45),*/
  department_id INTEGER,
  fullname VARCHAR(45),
  birthday DATETIME,
  salary INTEGER,
  CONSTRAINT FK_1 FOREIGN KEY (department_id) REFERENCES departments(id) ON DELETE CASCADE ON UPDATE CASCADE
);

